public class Variable {
    public static void main(String[] args){
        int a = 6;
        int b = 2;
        System.out.println("a + b = "+ (a + b));
        System.out.println("a - b = "+ (a - b));
        System.out.println("a * b = "+ (a * b));
        System.out.println("a / b = "+ (a / b));
        char d = 'a' + 3;
        System.out.println(d);
        long e = 384847522;
        long f = 988347273;
        System.out.println(e * f);
        boolean g = true;
        System.out.println(g);
    }
}
